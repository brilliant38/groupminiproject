package kr.co.dbs.dto;

public class check {
	private int h_code;
	private int h_date;
	private int h_number;
	private String h_state;
	public int getH_code() {
		return h_code;
	}
	public void setH_code(int h_code) {
		this.h_code = h_code;
	}
	public int getH_date() {
		return h_date;
	}
	public void setH_date(int h_date) {
		this.h_date = h_date;
	}
	public int getH_number() {
		return h_number;
	}
	public void setH_number(int h_number) {
		this.h_number = h_number;
	}
	public String getH_state() {
		return h_state;
	}
	public void setH_state(String h_state) {
		this.h_state = h_state;
	}
}
	
