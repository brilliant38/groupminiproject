package kr.co.dbs.dto;

public class customer {
	private String c_name;
	private String c_address;
	private int c_phone;
	private int c_weight;
	private String c_goods;
	private int c_goodsnumber;
	private String c_text;
	private String c_payment;
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_address() {
		return c_address;
	}
	public void setC_address(String c_address) {
		this.c_address = c_address;
	}
	public int getC_phone() {
		return c_phone;
	}
	public void setC_phone(int c_phone) {
		this.c_phone = c_phone;
	}
	public int getC_weight() {
		return c_weight;
	}
	public void setC_weight(int c_weight) {
		this.c_weight = c_weight;
	}
	public String getC_goods() {
		return c_goods;
	}
	public void setC_goods(String c_goods) {
		this.c_goods = c_goods;
	}
	public int getC_goodsnumber() {
		return c_goodsnumber;
	}
	public void setC_goodsnumber(int c_goodsnumber) {
		this.c_goodsnumber = c_goodsnumber;
	}
	public String getC_text() {
		return c_text;
	}
	public void setC_text(String c_text) {
		this.c_text = c_text;
	}
	public String getC_payment() {
		return c_payment;
	}
	public void setC_payment(String c_payment) {
		this.c_payment = c_payment;
	}
}
	
