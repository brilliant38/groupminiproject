package kr.co.dbs.dto;

public class Check {
	private int h_code;
	private String c_name;
	private String c_address;
	private String c_goods;
	private String c_date;
	private int h_number;
	private String h_state;
	private String m_name;
	public int getH_code() {
		return h_code;
	}
	public void setH_code(int h_code) {
		this.h_code = h_code;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_address() {
		return c_address;
	}
	public void setC_address(String c_address) {
		this.c_address = c_address;
	}
	public String getC_goods() {
		return c_goods;
	}
	public void setC_goods(String c_goods) {
		this.c_goods = c_goods;
	}
	public String getC_date() {
		return c_date;
	}
	public void setC_date(String c_date) {
		this.c_date = c_date;
	}
	public int getH_number() {
		return h_number;
	}
	public void setH_number(int h_number) {
		this.h_number = h_number;
	}
	public String getH_state() {
		return h_state;
	}
	public void setH_state(String h_state) {
		this.h_state = h_state;
	}
	public String getM_name() {
		return m_name;
	}
	public void setM_name(String m_name) {
		this.m_name = m_name;
	}
}
	
	
